'''
Python = Linguagem de programação
Tipo da tipagem: Dinâmica / forte
str -> string -> texto
Strings são textos que estão dentro de aspas.
'''
print(1234)

# Aspas simples
print('Marco Antônio')

# Aspas duplas
print("Marco Antônio")
print('Marco "Antônio"') # Caso você queira mostrar as aspas utilize este método. Aspas simples para mostrar que é string e aspa dupla

# Escape
print("Marco \"Antônio") # o caracter de escape é a barra invertida, e no caso ele irá pular o próximo caractere.

# r
print(r"Marco \"Antônio\"") # geralmente você não irá utilizar esses dois ultimos.